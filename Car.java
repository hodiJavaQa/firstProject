public class Car {
    //DATA
    String make;
    String model;
    String color;
    int numberOfWheels;
    int hp;
    int year;

    // func


    public Car(String make, String model, String color, int numberOfWheels, int hp, int year) {
        this.make = make;
        this.model = model;
        this.color = color;
        this.numberOfWheels = numberOfWheels;
        this.hp = hp;
        this.year = year;
    }
}
